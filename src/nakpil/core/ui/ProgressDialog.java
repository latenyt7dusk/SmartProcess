    public void setProcessObject(Object proc) {
        ProcessObj = proc;
    }

    public JProgressBar getProgressBar() {
        return PrPanel.getProgressBar();
    }

    public void start() {
        if (TRD == null) {
            if (ProcessObj instanceof Form2316Process) {
                TRD = new Thread((Form2316Process) ProcessObj);
                TRD.start();
            }
        }
    }

    public void stop() {
        if (TRD != null) {
            TRD.stop();
            TRD = null;
        }
        if (dialog != null) {
            dialog.dispose();
            dialog = null;
        }
    }

